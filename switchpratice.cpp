#include<iostream>
using namespace std;

int main(){
    int n;
    switch(n){
            case 1 : cout << "Pineapple" << endl;
            break;
            case 2 : cout << "Apple" << endl;
            break;
            case 3 : cout << "Grapes" << endl;
            break;
            case 4 : cout << "Watermellon" << endl;
            break;
            default : cout << "Not valid" << endl; 
    }
}